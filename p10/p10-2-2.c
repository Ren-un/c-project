void increment_date(int* y, int* m, int* d) {
    *d += 1;
    if ((*m == 1 || *m == 3 || *m == 5 || *m == 7 || *m == 10 || *m == 8 || *m == 12) && (*d == 32)) {
        *d = 1;
        *m += 1;
    }
    if ((*m == 4 || *m == 6  || *m == 9 || *m == 11 ) && (*d == 31)) {
        *d = 1;
        *m += 1;
    }
    if (*m == 2) {
        if (*d == 29&& ((*y & 4) != 0)) {
            *d = 1;
            *m += 1;
        }
        if (*d == 30 && ((*y & 4) == 0)) {
            *d = 1;
            *m += 1;
        }
    }
    if (*m == 13) {
        *y += 1;
        *m = 1;
    }
}